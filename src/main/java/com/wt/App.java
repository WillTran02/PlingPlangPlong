package com.wt;

public class App {
    public static String playPlingPlangPlong(int number) {
        if (number == 3) {
            return "Pling";
        } else if (number == 5) {
            return "Plang";
        } else if (number == 7) {
            return "Plong";
        }
        return "0";
    }
}
